package loop;

public class Loop {

    public static void main(String[] args) {
     
     
        System.out.println("Sequence of Number :");//output mengeluarkan kesemua looping number dari 99-11
        for (int i = 99; i>= 11; i-=11){//formula utk looping number
            System.out.println(" "+i);
        }
        
    }
    
}
